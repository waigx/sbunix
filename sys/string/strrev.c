#include <string.h>


char *
strrev(char *dest, const char *src)
{
	int i = strlen(src);
	char *dest_ptr = dest;

	dest[i] = '\0';
	for (i -= 1; i>=0; i--, dest_ptr++) {
		*dest_ptr = src[i];
	}

	return dest;
}
